//absolute
var number = -5;
var absoluteNumber = Math.abs(number);
console.log(absoluteNumber);

//round
var number = 5.5000000045;
var result = Math.round(number);
console.log(result);

//ceil
var number = 6.1111;
var result = Math.ceil(number);
console.log(result);

//floor
var number = 5.999999;
var result = Math.floor(number);
console.log(result);

//random
var number = 3.5000000045;
var result = Math.random(number);
console.log(result);

//random lottery
var number = Math.random() * 100;
var result = Math.round(number);
console.log(result);